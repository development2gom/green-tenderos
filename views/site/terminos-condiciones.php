            <p class="p9"><span class="s1">De tal forma que por gravedad al hacerlas girar caer&aacute; al fondo de cada urna alguna de las 10 esferas numeradas, las cuales, al estar en fila consecutiva, generar&aacute;n aleatoriamente un n&uacute;mero consecutivo formado por 8 d&iacute;gitos.</span></p>
            
            <p class="p9"><span class="s1">La formaci&oacute;n del n&uacute;mero se realizar&aacute; tomando como referencia la totalidad de registros de Folios participantes dentro de una base de datos, de forma tal que, si el n&uacute;mero total de registros alcanza ocho d&iacute;gitos, se colocar&aacute;n 8 &aacute;nforas con esferas numeradas del 0 al 9.</span></p>
            
            <p class="p9"><span class="s1">El n&uacute;mero a considerar para completar la cifra, ser&aacute; la que caiga en primer lugar al fondo de cada urna.</span></p>
            
            <p class="p9"><span class="s1">Lo anterior a efecto de poder celebrar el sorteo con la base de datos recabada conforme a los folios electr&oacute;nicos de participaci&oacute;n.</span></p>
            
            
